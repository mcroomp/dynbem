"""Drive OpenFAST AeroDyn standalone for a rotor described by a YAML.

The YAML schema mirrors verification/ccblade_docker/inputs/*.yaml as
closely as possible -- same rotor + polar + operating-point shape --
plus a `wake` section that controls AeroDyn's DBEMT / skew options.

For each operating point we:

  1. Write an aerodyn_driver .dvr file, an AeroDyn15 .dat file, a
     blade .dat file, and an airfoil .dat file under /tmp/<case>/.
  2. Invoke /openfast/install/bin/aerodyn_driver against the .dvr.
  3. Parse the resulting .out ASCII file, average the last full rotor
     revolution, and record (T, Q, P, CT, CP) into a per-rotor CSV.

The settling-then-average approach matches how dynbem.envelope drives
the dynamic-inflow models: spin up at fixed inputs until lambda
relaxes to steady state, then read off the converged values.

YAML schema:

    name: <free-form, used to name the output CSV>
    rotor:
      n_blades: int
      R_hub_m: float          # blade inboard cutout
      R_tip_m: float
      precone_deg: float      # optional, defaults 0
    air:
      rho: float              # optional, defaults 1.225
      mu: float               # optional, defaults 1.81206e-5
      a:   float              # speed of sound; optional, defaults 335
    blade_stations:
      - {r_m: float, chord_m: float, twist_deg: float}
      - ...                   # sorted by r_m; r_m is from rotor centre
    airfoil_polar:
      Re: float
      points:
        - {alpha_deg: float, cl: float, cd: float}
        - ...                 # cm column gets cm=0 added by the writer
    wake:
      WakeMod: 2              # 1=BEMT, 2=DBEMT, 3=OLAF; default 2
      DBEMT_Mod: 1            # 1=const tau1 (matches dynbem.oye Mod=1)
      tau1_const: 10.0        # s; only used for DBEMT_Mod=1
      SkewMod: 1              # 1=uncoupled, 2=Pitt-Peters; default 1
      TipLoss: true
      HubLoss: true
    operating_points:
      - {U_wind_ms: float, Omega_rpm: float, pitch_deg: float,
         settle_s: float}     # time to run before averaging
      - ...
"""
from __future__ import annotations

import argparse
import csv
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

import numpy as np
import yaml


AD_DRIVER = "/openfast/install/bin/aerodyn_driver"


# ---------------------------------------------------------------------------
# Input-file writers
# ---------------------------------------------------------------------------


def write_airfoil(path: Path, polar: dict) -> None:
    """Write a single AeroDyn15 airfoil .dat file (steady polar).

    The minimum-viable airfoil file: header + InterpOrd/NumCoords/etc.
    metadata + a single Re table with Alpha, Cl, Cd, Cm columns.
    """
    pts = polar["points"]
    re = polar.get("Re", 1.0e6)
    lines = [
        "! Airfoil polar generated by verification/openfast_docker/run.py",
        "! ------------------------------------------------------------------",
        '"DEFAULT"   InterpOrd        ! interpolation order',
        "1           NonDimArea       ! area for tabulated parameters",
        "0           NumCoords        ! number of points for blade shape",
        '"unused"    BL_file          ! boundary-layer file',
        "1           NumTabs          ! number of airfoil tables",
        "! ============== data for table 1 ==============",
        f"{re}      Re               ! Reynolds number",
        "0           UserProp         ! user-defined property",
        "False       InclUAdata       ! include unsteady-aero parameters",
        "! ----- polar -----",
        f"{len(pts)}   NumAlf          ! number of (alpha, Cl, Cd, Cm) rows",
        "!   Alpha       Cl          Cd          Cm",
        "!   (deg)       (-)         (-)         (-)",
    ]
    for p in pts:
        lines.append(
            f"   {p['alpha_deg']:>9.3f}   {p['cl']:>9.5f}   {p['cd']:>9.5f}   0.00000"
        )
    path.write_text("\n".join(lines) + "\n")


def write_blade(path: Path, stations: list[dict], af_id: int = 1) -> None:
    """Write an AeroDyn15 blade .dat file.

    BlSpn is the spanwise coordinate (m from blade root). dynbem and the
    YAML use r_m from rotor centre, so we subtract R_hub before writing.
    """
    rmin = min(s["r_m"] for s in stations)
    lines = [
        "------- AERODYN BLADE INPUT FILE -----------------------------------",
        "Generated by verification/openfast_docker/run.py",
        "======  Blade Properties ============================================",
        f"{len(stations)}                   NumBlNds   - Number of blade nodes",
        "    BlSpn        BlCrvAC      BlSwpAC      BlCrvAng     BlTwist       BlChord      BlAFID",
        "     (m)          (m)          (m)          (deg)         (deg)         (m)          (-)",
    ]
    for s in sorted(stations, key=lambda s: s["r_m"]):
        span = s["r_m"] - rmin
        lines.append(
            f"   {span:>9.5f}   {0.0:>9.5f}   {0.0:>9.5f}   {0.0:>9.5f}   "
            f"{s['twist_deg']:>9.5f}   {s['chord_m']:>9.5f}   {af_id:>4d}"
        )
    path.write_text("\n".join(lines) + "\n")


