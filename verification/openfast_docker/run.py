# ---------------------------------------------------------------------------
# Output parsing
# ---------------------------------------------------------------------------


def parse_out_text(path: Path) -> tuple[list[str], np.ndarray]:
    """Parse a text-format AeroDyn .out file.

    Returns (column_names, data) where data is shape (n_steps, n_cols).
    The file starts with several header lines, then a blank/dashes line,
    then a row of channel names, then a row of units, then the data.
    """
    lines = path.read_text().splitlines()
    # Find the channel header row (the one starting with "Time").
    for i, line in enumerate(lines):
        if line.strip().startswith("Time") and "(" not in line:
            header_idx = i
            break
    else:
        raise RuntimeError(f"Could not find header row in {path}")
    cols = lines[header_idx].split()
    # units line is header_idx + 1, data starts at header_idx + 2.
    data_lines = lines[header_idx + 2:]
    rows = []
    for ln in data_lines:
        s = ln.strip()
        if not s:
            continue
        rows.append([float(x) for x in s.split()])
    if not rows:
        raise RuntimeError(f"No data rows in {path}")
    return cols, np.array(rows)


def average_last_revolution(
    cols: list[str], data: np.ndarray, omega_rpm: float, settle_s: float,
) -> dict[str, float]:
    """Average channels over the last full revolution after `settle_s`."""
    time_idx = cols.index("Time")
    t = data[:, time_idx]
    t_settled = t >= settle_s
    if not t_settled.any():
        raise RuntimeError(
            f"All samples are within settling window ({settle_s} s); "
            f"max t = {t.max():.3f} s"
        )
    # One rotor revolution
    rev_period = 60.0 / max(omega_rpm, 1.0e-6)
    t_avg_start = max(t.max() - rev_period, t[t_settled].min())
    mask = t >= t_avg_start
    avg = {}
    for i, name in enumerate(cols):
        if name == "Time":
            continue
        avg[name] = float(data[mask, i].mean())
    return avg


# ---------------------------------------------------------------------------
# Per-case driver
# ---------------------------------------------------------------------------


def run_case(
    cfg: dict, op: dict, workdir: Path, case_idx: int,
) -> dict[str, float]:
    """Generate inputs, run aerodyn_driver, parse output for one OP."""
    n_blades = int(cfg["rotor"]["n_blades"])
    r_hub    = float(cfg["rotor"]["R_hub_m"])
    r_tip    = float(cfg["rotor"]["R_tip_m"])
    precone  = float(cfg["rotor"].get("precone_deg", 0.0))
    air      = cfg.get("air", {})
    wake     = cfg.get("wake", {})
    omega    = float(op["Omega_rpm"])
    pitch    = float(op["pitch_deg"])
    wind     = float(op["U_wind_ms"])
    settle   = float(op.get("settle_s", 5.0))
    # Time step: 72 azimuth steps per revolution (5 deg resolution) is
    # plenty for steady-state extraction. The image is built single-
    # precision (sqrt(eps) ~= 3.5e-4) and DBEMT_Init refuses to start
    # with dt below sqrt(eps); rev_period/72 = 1/(72*rpm/60) is well
    # above that for any sensible rotor speed.
    rev_period = 60.0 / max(omega, 1.0e-6)
    dt_s = max(rev_period / 72.0, 1.0e-3)
    # Total run time: settle + one extra revolution for averaging.
    tmax = settle + rev_period * 2.0

    case_dir = workdir / f"case_{case_idx:03d}"
    case_dir.mkdir(parents=True, exist_ok=True)

    airfoil_path = case_dir / "airfoil.dat"
    blade_path   = case_dir / "blade.dat"
    aero_path    = case_dir / "aerodyn.dat"
    dvr_path     = case_dir / "driver.dvr"

    write_airfoil(airfoil_path, cfg["airfoil_polar"])
    write_blade(blade_path, cfg["blade_stations"])
    write_aerodyn_primary(
        aero_path, wake, air,
        airfoil_rel=airfoil_path.name,
        blade_rel=blade_path.name,
        dt_s=dt_s,
    )
    write_driver(
        dvr_path, n_blades=n_blades, r_hub=r_hub, r_tip=r_tip,
        precone_deg=precone, omega_rpm=omega, pitch_deg=pitch,
        wind_ms=wind, tmax_s=tmax, dt_s=dt_s, air=air,
        aero_rel=aero_path.name,
    )

    # Invoke aerodyn_driver. Run in the case directory so relative
    # paths in the .dvr resolve correctly.
    proc = subprocess.run(
        [AD_DRIVER, dvr_path.name],
        cwd=case_dir,
        capture_output=True,
        text=True,
        timeout=300,
    )
    if proc.returncode != 0:
        sys.stderr.write(proc.stdout)
        sys.stderr.write(proc.stderr)
        raise RuntimeError(
            f"aerodyn_driver exited with code {proc.returncode} for case {case_idx}"
        )

    # Output file is <dvr_stem>.T1.out
    out_path = case_dir / f"{dvr_path.stem}.T1.out"
    if not out_path.exists():
        # Try other common locations
        cands = list(case_dir.glob("*.out"))
        if cands:
            out_path = cands[0]
        else:
            raise RuntimeError(
                f"aerodyn_driver did not produce a .out file in {case_dir}"
            )

    cols, data = parse_out_text(out_path)
    avg = average_last_revolution(cols, data, omega, settle)
    avg["case"] = case_idx
    avg["U_wind_ms"] = wind
    avg["Omega_rpm"] = omega
    avg["pitch_deg"] = pitch
    return avg


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--config", required=True)
    ap.add_argument("--output-dir", default="/out")
    ap.add_argument("--workdir", default=None,
                    help="Where to drop the per-case AeroDyn inputs/outputs. "
                         "Defaults to a tempdir; pass for debug.")
    args = ap.parse_args()

    cfg = yaml.safe_load(Path(args.config).read_text())
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    name = cfg["name"]

    if args.workdir:
        workdir = Path(args.workdir)
        workdir.mkdir(parents=True, exist_ok=True)
        keep_workdir = True
    else:
        workdir = Path(tempfile.mkdtemp(prefix="aerodyn_driver_"))
        keep_workdir = False

    print(f"Workdir: {workdir}")
    print(f"Cases: {len(cfg['operating_points'])}")

    rows = []
    try:
        for i, op in enumerate(cfg["operating_points"]):
            print(f"  [{i+1}/{len(cfg['operating_points'])}] "
                  f"U={op['U_wind_ms']} Omega={op['Omega_rpm']} pitch={op['pitch_deg']}",
                  flush=True)
            row = run_case(cfg, op, workdir, i)
            rows.append(row)
    finally:
        if not keep_workdir:
            shutil.rmtree(workdir, ignore_errors=True)

    # Write CSV
    out_path = out_dir / f"{name}_openfast.csv"
    if rows:
        cols = sorted({k for r in rows for k in r.keys()})
        # Sort so case/op columns come first
        head = ["case", "U_wind_ms", "Omega_rpm", "pitch_deg"]
        cols = head + [c for c in cols if c not in head]
        with out_path.open("w", newline="") as fh:
            w = csv.DictWriter(fh, fieldnames=cols)
            w.writeheader()
            w.writerows(rows)
        print(f"Wrote {out_path}")
    else:
        print("No rows produced.")
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
